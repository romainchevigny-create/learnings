let allLearnings = [];
let currentFilter = 'all';
let currentFocusIndex = 0;
let focusMode = false;

function loadSavedData() {
  const saved = localStorage.getItem('learnings_data');
  return saved ? JSON.parse(saved) : {};
}

function saveData(data) {
  localStorage.setItem('learnings_data', JSON.stringify(data));
}

let savedData = loadSavedData();

function generateId(themeIdx, subIdx, learnIdx) {
  return `l_${themeIdx}_${subIdx}_${learnIdx}`;
}

function renderContent() {
  const content = document.getElementById('content');
  const focusContainer = document.getElementById('focus-container');
  
  document.querySelectorAll('.theme-section').forEach(el => el.remove());
  
  allLearnings = [];
  
  learningsData.forEach((theme, themeIdx) => {
    const section = document.createElement('div');
    section.className = 'theme-section';
    section.dataset.themeIdx = themeIdx;
    
    const header = document.createElement('div');
    header.className = 'theme-header';
    const h2 = document.createElement('h2');
    h2.textContent = theme.icon + ' ' + theme.theme;
    const toggleIcon = document.createElement('span');
    toggleIcon.className = 'toggle-icon';
    toggleIcon.textContent = '▼';
    header.appendChild(h2);
    header.appendChild(toggleIcon);
    header.addEventListener('click', () => {
      section.classList.toggle('collapsed');
    });

    const themeContent = document.createElement('div');
    themeContent.className = 'theme-content';

    theme.subthemes.forEach((subtheme, subIdx) => {
      const subDiv = document.createElement('div');
      subDiv.className = 'subtheme';
      const h3 = document.createElement('h3');
      h3.textContent = subtheme.name;
      subDiv.appendChild(h3);

      subtheme.learnings.forEach((learning, learnIdx) => {
        const id = generateId(themeIdx, subIdx, learnIdx);
        const isTed = typeof learning === 'object' && learning.ted;
        const text = isTed ? learning.text : learning;

        const learningData = savedData[id] || { read: false, favorite: false, note: '' };

        const learningDiv = document.createElement('div');
        learningDiv.className = 'learning';
        if (isTed) learningDiv.classList.add('ted-talk');
        if (learningData.read) learningDiv.classList.add('read');
        if (learningData.favorite) learningDiv.classList.add('favorite');
        learningDiv.dataset.id = id;
        learningDiv.dataset.themeIdx = themeIdx;
        learningDiv.dataset.text = text.replace(/<[^>]*>/g, '').toLowerCase();

        // Learning text
        const learningText = document.createElement('div');
        learningText.className = 'learning-text';
        const tempDiv = document.createElement('div');
        tempDiv.textContent = text;
        // Si c'est un TED talk avec du HTML, on utilise un template
        if (isTed) {
          const tpl = document.createElement('template');
          tpl.innerHTML = text;
          learningText.appendChild(tpl.content.cloneNode(true));
        } else {
          learningText.textContent = text;
        }

        // Actions
        const actionsDiv = document.createElement('div');
        actionsDiv.className = 'learning-actions';

        const readBtn = document.createElement('button');
        readBtn.className = 'action-btn read-btn' + (learningData.read ? ' active' : '');
        readBtn.dataset.action = 'read';
        readBtn.textContent = learningData.read ? '✓ Lu' : 'Marquer lu';

        const favBtn = document.createElement('button');
        favBtn.className = 'action-btn favorite' + (learningData.favorite ? ' active' : '');
        favBtn.dataset.action = 'favorite';
        favBtn.textContent = '⭐ Favori';

        const noteBtn = document.createElement('button');
        noteBtn.className = 'action-btn note-btn' + (learningData.note ? ' active' : '');
        noteBtn.dataset.action = 'note';
        noteBtn.textContent = learningData.note ? '📝 Note' : '📝 Ajouter note';

        actionsDiv.appendChild(readBtn);
        actionsDiv.appendChild(favBtn);
        actionsDiv.appendChild(noteBtn);

        // Note section
        const noteSection = document.createElement('div');
        noteSection.className = 'note-section' + (learningData.note ? ' visible' : '');

        const textarea = document.createElement('textarea');
        textarea.className = 'note-textarea';
        textarea.placeholder = 'Vos réflexions personnelles sur ce learning...';
        textarea.value = learningData.note || '';
        noteSection.appendChild(textarea);

        learningDiv.appendChild(learningText);
        learningDiv.appendChild(actionsDiv);
        learningDiv.appendChild(noteSection);

        readBtn.addEventListener('click', () => { toggleRead(id); });
        favBtn.addEventListener('click', () => { toggleFavorite(id); });
        noteBtn.addEventListener('click', () => { noteSection.classList.toggle('visible'); });
        textarea.addEventListener('input', (e) => { updateNote(id, e.target.value); });
        
        subDiv.appendChild(learningDiv);
        
        allLearnings.push({
          id,
          themeIdx,
          subIdx,
          learnIdx,
          text,
          theme: theme.theme,
          subtheme: subtheme.name,
          isTed
        });
      });
      
      themeContent.appendChild(subDiv);
    });
    
    section.appendChild(header);
    section.appendChild(themeContent);
    content.insertBefore(section, focusContainer);
  });
  
  updateStats();
}

function toggleRead(id) {
  if (!savedData[id]) savedData[id] = { read: false, favorite: false, note: '' };
  savedData[id].read = !savedData[id].read;
  saveData(savedData);
  
  const learningEl = document.querySelector(`[data-id="${id}"]`);
  learningEl.classList.toggle('read');
  const btn = learningEl.querySelector('[data-action="read"]');
  btn.classList.toggle('active');
  btn.textContent = savedData[id].read ? '✓ Lu' : 'Marquer lu';
  
  updateStats();
  applyFilter();
}

function toggleFavorite(id) {
  if (!savedData[id]) savedData[id] = { read: false, favorite: false, note: '' };
  savedData[id].favorite = !savedData[id].favorite;
  saveData(savedData);
  
  const learningEl = document.querySelector(`[data-id="${id}"]`);
  learningEl.classList.toggle('favorite');
  learningEl.querySelector('[data-action="favorite"]').classList.toggle('active');
  
  updateStats();
  applyFilter();
}

function updateNote(id, note) {
  if (!savedData[id]) savedData[id] = { read: false, favorite: false, note: '' };
  savedData[id].note = note;
  saveData(savedData);
  
  const learningEl = document.querySelector(`[data-id="${id}"]`);
  const btn = learningEl.querySelector('[data-action="note"]');
  if (note.trim()) {
    btn.classList.add('active');
    btn.textContent = '📝 Note';
  } else {
    btn.classList.remove('active');
    btn.textContent = '📝 Ajouter note';
  }
  
  updateStats();
}

function updateStats() {
  const total = allLearnings.length;
  let readCount = 0;
  let favCount = 0;
  let notesCount = 0;
  
  allLearnings.forEach(l => {
    const data = savedData[l.id] || {};
    if (data.read) readCount++;
    if (data.favorite) favCount++;
    if (data.note && data.note.trim()) notesCount++;
  });
  
  document.getElementById('total-count').textContent = total;
  document.getElementById('read-count').textContent = readCount;
  document.getElementById('fav-count').textContent = favCount;
  document.getElementById('notes-count').textContent = notesCount;
  
  const progress = total > 0 ? (readCount / total) * 100 : 0;
  document.getElementById('progress-fill').style.width = progress + '%';
}

function applyFilter() {
  const searchTerm = document.getElementById('search').value.toLowerCase();
  const learnings = document.querySelectorAll('.learning');
  
  learnings.forEach(learning => {
    const id = learning.dataset.id;
    const data = savedData[id] || {};
    const text = learning.dataset.text;
    
    let visible = true;
    
    if (currentFilter === 'unread' && data.read) visible = false;
    if (currentFilter === 'favorites' && !data.favorite) visible = false;
    if (currentFilter === 'with-notes' && (!data.note || !data.note.trim())) visible = false;
    
    if (searchTerm && !text.includes(searchTerm)) visible = false;
    
    learning.style.display = visible ? '' : 'none';
  });
  
  document.querySelectorAll('.subtheme').forEach(sub => {
    const visibleLearnings = sub.querySelectorAll('.learning:not([style*="display: none"])');
    sub.style.display = visibleLearnings.length > 0 ? '' : 'none';
  });
  
  document.querySelectorAll('.theme-section').forEach(section => {
    const visibleSubs = section.querySelectorAll('.subtheme:not([style*="display: none"])');
    section.style.display = visibleSubs.length > 0 ? '' : 'none';
  });
}

function enterFocusMode() {
  focusMode = true;
  document.body.classList.add('focus-mode');
  document.getElementById('focus-container').style.display = 'block';
  
  const firstUnread = allLearnings.findIndex(l => {
    const data = savedData[l.id] || {};
    return !data.read;
  });
  currentFocusIndex = firstUnread >= 0 ? firstUnread : 0;
  
  displayFocusCard();
}

function exitFocusMode() {
  focusMode = false;
  document.body.classList.remove('focus-mode');
  document.getElementById('focus-container').style.display = 'none';
  
  document.querySelectorAll('.theme-section').forEach(s => s.style.display = '');
  applyFilter();
}

function displayFocusCard() {
  const learning = allLearnings[currentFocusIndex];
  if (!learning) return;
  
  const data = savedData[learning.id] || {};
  
  document.getElementById('focus-category').textContent = `${learning.theme} → ${learning.subtheme}`;
  const focusTextEl = document.getElementById('focus-text');
  focusTextEl.innerHTML = '';
  const tpl3 = document.createElement('template');
  tpl3.innerHTML = learning.text;
  focusTextEl.appendChild(tpl3.content.cloneNode(true));
  document.getElementById('focus-counter').textContent = `${currentFocusIndex + 1} / ${allLearnings.length}`;
  
  const readBtn = document.getElementById('focus-toggle-read');
  readBtn.textContent = data.read ? '✓ Marqué lu' : 'Marquer lu ✓';
  readBtn.style.background = data.read ? '#48bb78' : '#667eea';
  
  const favBtn = document.getElementById('focus-toggle-fav');
  favBtn.textContent = data.favorite ? '⭐ Favori ✓' : '⭐ Favori';
  favBtn.style.background = data.favorite ? '#f6ad55' : '#e2e8f0';
  favBtn.style.color = data.favorite ? 'white' : '#2d3748';
}

document.getElementById('search').addEventListener('input', applyFilter);

document.querySelectorAll('.filter-btn[data-filter]').forEach(btn => {
  btn.addEventListener('click', () => {
    document.querySelectorAll('.filter-btn[data-filter]').forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
    currentFilter = btn.dataset.filter;
    applyFilter();
  });
});

document.getElementById('focus-mode-btn').addEventListener('click', enterFocusMode);
document.getElementById('focus-exit').addEventListener('click', exitFocusMode);

document.getElementById('focus-prev').addEventListener('click', () => {
  if (currentFocusIndex > 0) {
    currentFocusIndex--;
    displayFocusCard();
  }
});

document.getElementById('focus-next').addEventListener('click', () => {
  if (currentFocusIndex < allLearnings.length - 1) {
    currentFocusIndex++;
    displayFocusCard();
  }
});

document.getElementById('focus-toggle-read').addEventListener('click', () => {
  const learning = allLearnings[currentFocusIndex];
  toggleRead(learning.id);
  displayFocusCard();
});

document.getElementById('focus-toggle-fav').addEventListener('click', () => {
  const learning = allLearnings[currentFocusIndex];
  toggleFavorite(learning.id);
  displayFocusCard();
});

document.addEventListener('keydown', (e) => {
  if (!focusMode) return;
  if (e.key === 'ArrowLeft') document.getElementById('focus-prev').click();
  if (e.key === 'ArrowRight') document.getElementById('focus-next').click();
  if (e.key === 'Escape') exitFocusMode();
  if (e.key === ' ') {
    e.preventDefault();
    document.getElementById('focus-toggle-read').click();
  }
});

document.getElementById('reset-btn').addEventListener('click', () => {
  if (confirm('⚠️ Êtes-vous sûr de vouloir effacer toutes vos données (lectures, favoris, notes) ?\n\nCette action est irréversible.')) {
    localStorage.removeItem('learnings_data');
    savedData = {};
    renderContent();
  }
});

// Export Anki CSV
document.getElementById('anki-btn').addEventListener('click', () => {
  const favorites = allLearnings.filter(l => {
    const data = savedData[l.id] || {};
    return data.favorite;
  });

  if (favorites.length === 0) {
    alert('⭐ Vous n\'avez pas encore de favoris !\n\nMarquez des learnings avec ⭐ pendant votre lecture, puis revenez générer votre deck.');
    return;
  }

  let csv = '';

  favorites.forEach(l => {
    const tag = l.theme.replace(/[^a-zA-Z0-9]/g, '_').replace(/^[0-9]+_/, '') + '::' + l.subtheme.replace(/[^a-zA-Z0-9]/g, '_');

    // Recto : thème + sous-thème
    const recto = `<b>${l.theme}</b><br><i>${l.subtheme}</i>`;

    // Verso : texte original intégral, exactement tel quel dans le fichier HTML
    const verso = l.text;

    csv += `"${recto.replace(/"/g, '""')}";"${verso.replace(/"/g, '""')}";"${tag}"\n`;
  });

  const blob = new Blob(['\uFEFF' + csv], { type: 'text/csv;charset=utf-8;' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = 'Learnings_Anki.csv';
  a.click();
  URL.revokeObjectURL(url);

  alert(`✅ Deck généré avec ${favorites.length} cartes !\n\nPour l'importer dans Anki :\n1. Ouvrez Anki\n2. Fichier → Importer\n3. Sélectionnez "Learnings_Anki.csv"\n4. Séparateur : point-virgule\n5. Activez "Autoriser HTML dans les champs"\n6. Importez !`);
});

renderContent();
